numero1 = int(input("insira o valor de 1: "))
numero2 = int(input("insira o valor de 2: "))

if numero1 > numero2:
    diferença1 = numero1 - numero2
    print(f"A diferença entre os valores é de {diferença1}")
else:
    diferença2 = numero2 - numero1
    print(f"A diferença entre os valores é de {diferença2}")